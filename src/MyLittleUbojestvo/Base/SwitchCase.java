package src.MyLittleUbojestvo.Base;

public class SwitchCase {
    public static void main(String[] args) {
        int list = 5;

        switch (list){
            case 1:
                System.out.println("FLOWER");
                break;
            case 2:
                System.out.println("FLAUR");
                break;
            case 3:
                System.out.println("FLOOR");
                break;
            case 4:
                System.out.println("FLAPPY");
            case 5:
                System.out.println("Gotch u");
        }
    }
}
